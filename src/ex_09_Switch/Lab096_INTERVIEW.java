package ex_09_Switch;

public class Lab096_INTERVIEW {
    public static void main(String[] args)
    {
        int code='C';
        switch(code)
        {
            default: System.out.println("Hellooooo");
            break;

            case 'A':System.out.println("65");
            break;

            case 'B': System.out.println("66");
            break;

        }
    }
}


